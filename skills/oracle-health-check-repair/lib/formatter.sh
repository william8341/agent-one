#!/usr/bin/env bash

format_json_result() {
    local check_id="$1"
    local check_name="$2"
    local status="$3"
    local message="$4"
    local details="$5"
    local need_fix="$6"
    local fix_script="$7"
    
    cat <<EOF
{
  "id": "$check_id",
  "name": "$check_name",
  "status": "$status",
  "message": "$message",
  "details": $details,
  "need_fix": $need_fix,
  "fix_script": "$fix_script"
}
EOF
}

format_fix_result() {
    local fix_name="$1"
    local status="$2"
    local message="$3"
    
    cat <<EOF
{
  "script": "$fix_name",
  "status": "$status",
  "message": "$message"
}
EOF
}

parse_check_result() {
    local result="$1"
    local key="$2"
    
    echo "$result" | jq -r ".$key // empty" 2>/dev/null
}

merge_results() {
    local base="$1"
    local new="$2"
    
    echo "$base" | jq -s ".[0] * .[1]" - <(echo "$new") 2>/dev/null || echo "$new"
}

generate_markdown_report() {
    local json_result="$1"
    local output_file="$2"
    
    local check_time=$(echo "$json_result" | jq -r '.check_time')
    local host=$(echo "$json_result" | jq -r '.host')
    local sid=$(echo "$json_result" | jq -r '.sid')
    local overall_status=$(echo "$json_result" | jq -r '.overall_status')
    local issues_count=$(echo "$json_result" | jq -r '.issues_found')
    local fixes_count=$(echo "$json_result" | jq -r '.fixes_applied | length')
    
    local status_icon="✅"
    case "$overall_status" in
        "CRITICAL") status_icon="❌" ;;
        "WARNING") status_icon="⚠️" ;;
        "OK") status_icon="✅" ;;
    esac
    
    cat > "$output_file" <<EOF
# Oracle Health Check Report

**Time**: $check_time  
**Host**: $host ($sid)  
**Status**: $status_icon $overall_status

## Summary

- Issues Found: $issues_count
- Fixes Applied: $fixes_count

## Check Results

| ID | Check | Status | Message |
|----|-------|--------|---------|
EOF
    
    echo "$json_result" | jq -r '.checks[] | "| \(.id) | \(.name) | \(.status) | \(.message) |"' >> "$output_file"
    
    if [[ "$fixes_count" -gt 0 ]]; then
        cat >> "$output_file" <<EOF

## Fixes Applied

| Script | Status | Message |
|--------|--------|---------|
EOF
        echo "$json_result" | jq -r '.fixes_applied[] | "| \(.script) | \(.status) | \(.message) |"' >> "$output_file"
    fi
    
    cat >> "$output_file" <<EOF

---
Generated by Oracle Health Check + Auto-Repair
EOF
}
